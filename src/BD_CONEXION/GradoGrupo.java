package BD_CONEXION;

public class GradoGrupo {
    public int Id;
    public int IdDocente;
    public int Grado;
    public String Grupo;

    public GradoGrupo(int Id, int IdDocente, int Grado, String Grupo){
        this.Id = Id;
        this.IdDocente = IdDocente;
        this.Grado = Grado;
        this.Grupo = Grupo;
    }
}
